let currentLanguage = localStorage.getItem('language') || 'ar';

function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // تحديث اتجاه الصفحة
    const htmlElement = document.documentElement;
    const bodyElement = document.body;
    
    if (lang === 'ar' || lang === 'ur' || lang === 'fa') {
        htmlElement.lang = lang;
        htmlElement.dir = 'rtl';
        bodyElement.dir = 'rtl';
    } else if (lang === 'zh' || lang === 'zh-SG') {
        htmlElement.lang = 'zh';
        htmlElement.dir = 'ltr';
        bodyElement.dir = 'ltr';
    } else {
        htmlElement.lang = lang;
        htmlElement.dir = 'ltr';
        bodyElement.dir = 'ltr';
    }
    
    // تحديث النصوص
    translatePage();
    
    // تحديث الزر النشط
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
}

function getTranslation(key) {
    const langData = translations[currentLanguage] || translations['ar'];
    return langData[key] || translations['ar'][key] || key;
}

function translatePage() {
    // ترجمة العناصر التي تحتوي على data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = getTranslation(key);
    });
}

// تهيئة اللغة عند تحميل الصفحة
document.addEventListener('DOMContentLoaded', function() {
    const languageBtn = document.getElementById('languageBtn');
    const languageDropdown = document.getElementById('languageDropdown');
    
    // تعيين اللغة الحالية
    const currentLangBtn = document.querySelector(`[data-lang="${currentLanguage}"]`);
    if (currentLangBtn) {
        currentLangBtn.classList.add('active');
    }
    
    // فتح/إغلاق قائمة اللغات
    if (languageBtn) {
        languageBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            languageDropdown.classList.toggle('active');
        });
    }
    
    // تغيير اللغة
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.stopPropagation();
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
            languageDropdown.classList.remove('active');
        });
    });
    
    // إغلاق القائمة عند النقر خارجها
    document.addEventListener('click', function() {
        languageDropdown.classList.remove('active');
    });
});
